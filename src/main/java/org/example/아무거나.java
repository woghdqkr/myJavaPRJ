package org.example;

public class 아무거나 {
    public static void main(String[] args) {
        int i, j, k;
        for(j=0; j<5; j++){
            for(i=0; i<5; i--){
                System.out.printf("  ");
            }
            for(k=3; k<6; k++){
                System.out.printf("*");
            }
        }
    }
}
